import type { ScenePayload, StoryNode } from "./types";

const baseSafety = {
  contains_open_chat: false,
  asks_personal_data: false,
  out_of_sandbox: false,
};

const baseAccessibility = {
  reading_level: "standard" as const,
  max_words_ok: true,
};

function scene(
  node_key: string,
  room_slug: string,
  character: ScenePayload["character"],
  character_state: ScenePayload["character_state"],
  speaker_name: string,
  text: string,
  read_again_text: string,
  choices: ScenePayload["choices"],
  clue?: string,
  backpack?: ScenePayload["backpack_prompt"],
): ScenePayload {
  return {
    scene_id: `fallback-${node_key}`,
    node_key,
    room_slug,
    language: "en",
    character,
    character_state,
    dialogue: { speaker_name, text, read_again_text },
    choices,
    backpack_prompt: backpack,
    clue: clue ? { available: true, text: clue } : { available: false, text: "" },
    consequence_preview: { show_after_choice: true, tone: "debug" },
    accessibility: baseAccessibility,
    safety_flags: baseSafety,
  };
}

function choices(items: Array<[ScenePayload["choices"][number]["id"], string, ScenePayload["choices"][number]["choice_type"]]>): ScenePayload["choices"] {
  return items.map(([id, text, choice_type]) => ({ id, text, choice_type }));
}

